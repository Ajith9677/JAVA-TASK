package EmployeeInfo;

public class Employees {
	public static void main(String[] args) {
		
		int a =10;
		int b= 2;
		
		int c= a+b;
		int d= a-b;
		int e= a*b;
		int f= a/b;
		int g= a%b;
		int h= a++;
		int i= a--;
		int j= ++a;
		int k= --a;
		
		System.out.println("Addition value is :"+c+"\t");
		System.out.println("Substraction value is :"+d+"\t");
		System.out.println("Multiplication value is :"+e+"\t");
		System.out.println("Division value is :"+f+"\t");
		System.out.println("modular value is :"+g+"\t");
		System.out.println("Increment value is:"+h+"\t");
		System.out.println("Decrement value is:"+i+"\t");
		System.out.println("Pre increment value is:"+j+"\t");
		System.out.println("Post increment value is:"+k+"\t");
		
	}
		
	

}
